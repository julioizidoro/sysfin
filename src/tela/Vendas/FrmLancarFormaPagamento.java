    public void visualizarFormaPagamento(Formapagamento forma){
        tipoDocumentojComboBox.setEnabled(false);
        tipoDocumentojComboBox.setSelectedItem(forma.getTipoDocumento());
        nomejTextField.setText(forma.getNomeCliente());
        cpfjTextField.setText(forma.getCpf());
        if (forma.getTipoDocumento().equalsIgnoreCase("Boleto")){
            tipoLogradourojComboBox.setSelectedItem(forma.getTipoLogradouro());
            logradourojTextField.setText(forma.getLogradouro());
            numerojTextField.setText(forma.getNumero());
            complementojTextField.setText(forma.getComplemento());
            bairrojTextField.setText(forma.getBairro());
            cidadejTextField.setText(forma.getCidade());
            estadojComboBox.setSelectedItem(forma.getEstado());
            cepjFormattedTextField.setText(forma.getCep());
        }
        numeroParcelasjTextField.setText(String.valueOf(forma.getNumeroParcelas()));
        numeroParcelasjTextField.setEnabled(false);
        dataVencimentojDateChooser.setDate(forma.getDataVencimento());
        dataVencimentojDateChooser.setEnabled(false);
        valorjTextField.setText(Formatacao.foramtarFloatString(forma.getValor()));
        valorjTextField.setEnabled(false);
        valorParcelajTextField.setText(Formatacao.foramtarFloatString(forma.getValorParcela()));
        valorParcelajTextField.setEnabled(false);
        observacaojTextArea.setText(forma.getObservacao());
        observacaojTextArea.setEnabled(false);
    }

