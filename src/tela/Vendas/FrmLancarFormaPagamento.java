    @Override
    public void setArquivo(File arquivo) {
        if (arquivo!=null){
            arquivojTextField.setText(arquivo.getAbsolutePath() + " " + arquivo.getName());
            try {
                this.formaPagamento.setComprovante(gerarArquivoBase(arquivo));
            } catch (IOException ex) {
                Logger.getLogger(FrmCadastarContasPagar.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Erro Carregar Arquivo " + ex);
            }
        }
    }
    
    public byte[] gerarArquivoBase(File file) throws IOException {
        InputStream is = new FileInputStream(file);
        byte[] xml = new byte[(int) file.length()];
        int offset = 0;
        int numRead = 0;
        while (offset < xml.length
                && (numRead = is.read(xml, offset, xml.length - offset)) >= 0) {
            offset += numRead;
        }
        return xml;
    }

