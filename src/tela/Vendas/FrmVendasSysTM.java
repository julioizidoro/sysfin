    public void setModel(){
        IntegracaoController integracaoController = new IntegracaoController();
        listaVendas = integracaoController.consultarVendas();
        if (listaVendas==null){
            listaVendas = new ArrayList<Viewvendasintegracao>();
        }
        modelVendas = new VendasSysTMTableModel(listaVendas);
        vendasjTable.setModel(modelVendas);
        DefaultTableCellRenderer rendererValor = new DefaultTableCellRenderer();
        rendererValor.setHorizontalAlignment(SwingConstants.RIGHT);
        vendasjTable.getColumnModel().getColumn(0).setPreferredWidth(80);
        vendasjTable.getColumnModel().getColumn(1).setPreferredWidth(80);
        vendasjTable.getColumnModel().getColumn(2).setPreferredWidth(200);
        vendasjTable.getColumnModel().getColumn(3).setPreferredWidth(200);
        vendasjTable.getColumnModel().getColumn(4).setCellRenderer(rendererValor);
        vendasjTable.getColumnModel().getColumn(4).setPreferredWidth(100);
        vendasjTable.getColumnModel().getColumn(5).setPreferredWidth(200);
        vendasjTable.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);  
        vendasjTable.repaint();
    }



