    private void adicionarAPagarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarAPagarjButtonActionPerformed
        Float totalLiquido = 0.0f;
        Float pagametnoFornecedor = 0.0f;
        if (totalLiquidojTextField.getText().length()>0){
            totalLiquido = Formatacao.ConvercaoMonetariaFloat(totalLiquidojTextField.getText());
        }
        if (valorPagoFornecedorjTextField.getText().length()>0){
            pagametnoFornecedor= Formatacao.ConvercaoMonetariaFloat(valorPagoFornecedorjTextField.getText());
        }
        Float total = totalLiquido - pagametnoFornecedor;
        new FrmLancarFormaPagamento(clientejTextField.getText(), Formatacao.foramtarFloatString(total), this);
    }//GEN-LAST:event_adicionarAPagarjButtonActionPerformed

    private void excluirAPagarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirAPagarjButtonActionPerformed
        int linha = formajTable.getSelectedRow();
        if (linha>=0){
            if (listaForma.get(linha).getIdformaPagamento()!=null){
                FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
                formaPagamentoController.Excluir(listaForma.get(linha).getIdformaPagamento());
                listaForma.remove(linha);
            }else {
                listaForma.remove(linha);
            }
            carregarFormaPagamento();
        }else JOptionPane.showMessageDialog(rootPane, " Selecione uma Forma de Pagamento");
    }//GEN-LAST:event_excluirAPagarjButtonActionPerformed

