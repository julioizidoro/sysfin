    private void calcularValoresTotais(){
        float comissaoLiquida = 0.0f;
        float despesasFinanceiras = 0.0f;
        float comissaoFuncionarios = 0.0f;
        float comissaoTerceiros = 0.0f;
        float valorBruto = 0.0f;
        float valorDesconto = 0.0f;
        float totalLiquido = 0.0f;
        float valorFornecedor = 0.0f;
        float liquidoVendas = 0.0f;
        float pagarReceber = 0.0f;
        if (totalBrutojTextField.getText().length()>0){
            valorBruto = Formatacao.formatarStringfloat(totalBrutojTextField.getText());
        }
        if (valorDescontojTextField.getText().length()>0){
            valorDesconto = Formatacao.formatarStringfloat(valorDescontojTextField.getText());
        }
        totalLiquido =  valorBruto - valorDesconto;
        totalLiquidojTextField.setText(Formatacao.foramtarFloatString(totalLiquido));
        totalVendajTextField.setText(Formatacao.foramtarFloatString(totalLiquido));
        if (comissaoLiquidajTextField.getText().length()>0){
            comissaoLiquida = Formatacao.formatarStringfloat(comissaoLiquidajTextField.getText());
        }
        if (despesasFinanceirajTextField.getText().length()>0){
            despesasFinanceiras = Formatacao.formatarStringfloat(despesasFinanceirajTextField.getText());
        }
        if (comissaoFuncionariojTextField.getText().length()>0){
            comissaoFuncionarios = Formatacao.formatarStringfloat(comissaoFuncionariojTextField.getText());
        }
        if (comissaoTerceirosjTextField.getText().length()>0){
            comissaoTerceiros = Formatacao.formatarStringfloat(comissaoTerceirosjTextField.getText());
        }
        liquidoVendas = comissaoLiquida - (despesasFinanceiras + comissaoFuncionarios + comissaoTerceiros);
        if (valorPagoFornecedorjTextField.getText().length()>0){
            valorFornecedor = Formatacao.formatarStringfloat(valorPagoFornecedorjTextField.getText());
        }
        pagarReceber = valorBruto - (comissaoLiquida + valorFornecedor);
        LiquidoVendaojTextField.setText(Formatacao.foramtarFloatString(liquidoVendas));
        pagarReceberjTextField.setText(Formatacao.foramtarFloatString(pagarReceber));
    }

