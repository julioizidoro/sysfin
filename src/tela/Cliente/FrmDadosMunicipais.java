    public void preecherDadosMunicipais(){
        if (cliente!=null){
            DadosMunicipaisController dadosMunicipaisController = new DadosMunicipaisController();
            this.dadosmunicipais = dadosMunicipaisController.consultar(cliente.getIdcliente());
            if (dadosmunicipais!=null){
                aplicativojTextField.setText(dadosmunicipais.getAplicativo());
                usuariojTextField.setText(dadosmunicipais.getUsuairo());
                senhajTextField.setText(dadosmunicipais.getSenha());
                inscricaoMunicipaljTextField.setText(dadosmunicipais.getInscricaomunicipal());
            }else {
                dadosmunicipais = new Dadosmunicipais();
            }
        }
    }

