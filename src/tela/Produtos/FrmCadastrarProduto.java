    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        if (produto==null){
            produto = new Produto();
        }
        produto.setDescricao(nomejTextField.getText());
        produto.setCliente(cliente.getIdcliente());
        if (codigosystmjTextField.getText().length()>0){
            produto.setCodigosystm(Integer.valueOf(codigosystmjTextField.getText()));
        }else produto.setCodigosystm(0);
        
        ProdutoController produtoController = new ProdutoController();
        produtoController.salvar(produto);
        this.telaProduto.setModel();
        this.dispose();
    }//GEN-LAST:event_salvarjButtonActionPerformed

