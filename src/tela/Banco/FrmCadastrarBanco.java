    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        if (banco==null){
            banco = new Banco();
        }
        banco.setNome(nomejTextField.getText());
        banco.setNumeroBanco(numerojTextField.getText());
        banco.setAgencia(agenciajTextField.getText());
        banco.setConta(contajTextField.getText());
        banco.setCahve(chavejTextField.getText());
        banco.setSenha(senhajTextField.getText());
        banco.setGerente(gerentejTextField.getText());
        banco.setEmailGerente(emailjTextField.getText());
        banco.setCliente(cliente.getIdcliente());
        BancoController bancoController = new BancoController();
        bancoController.salvar(banco);
        this.telaBanco.setModel();
        this.dispose();
    }//GEN-LAST:event_salvarjButtonActionPerformed

